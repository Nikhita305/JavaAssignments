class Ice {
    public static void melt() {
        System.out.println("Ice is melting.");
    }

    public static void freeze() {
        System.out.println("Water is freezing into ice.");
    }

    public static void crack() {
        System.out.println("Ice is cracking.");
    }

    public static void main(String[] args) {
        melt();
        freeze();
        crack();
    }
}
