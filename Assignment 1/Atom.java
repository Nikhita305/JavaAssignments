class Atom {
    public static void split() {
        System.out.println("Atom is split.");
    }

    public static void combine() {
        System.out.println("Atoms are combined.");
    }

    public static void energyRelease() {
        System.out.println("Energy is released from the atom.");
    }

    public static void main(String[] args) {
        split();
        combine();
        energyRelease();
    }
}
