class Globe {
    public static void rotate() {
        System.out.println("Globe is rotating.");
    }

    public static void showCountries() {
        System.out.println("Displaying all countries on the globe.");
    }

    public static void zoomIn() {
        System.out.println("Zooming in on a country.");
    }

    public static void main(String[] args) {
        rotate();
        showCountries();
        zoomIn();
    }
}
