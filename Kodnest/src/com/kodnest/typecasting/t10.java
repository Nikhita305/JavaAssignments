package com.kodnest.typecasting;

public class t10 {
    public static void main(String[] args) {
        int num = 5;
        for(int i=1; i<=10; i++){
            System.out.println(num+" * "+ i + " = "+(num*i));
        }
        int i = 1;
        while(i<=10){
            System.out.println(num+" * "+ i + " = "+(num*i));
            i++;
        }
    }
}
