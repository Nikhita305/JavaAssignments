package com.inheritance;

public class Language {
    public Language() {
        System.out.println("Running no-arg constructor of Language");
    }

    public void name() {
        System.out.println("This is a language");
    }

    public void origin() {
        System.out.println("Languages have different historical origins");
    }

    public void script() {
        System.out.println("Each language has its own script");
    }

    public void speakers() {
        System.out.println("Languages are spoken by people around the world");
    }

    public void importance() {
        System.out.println("Language is important for communication");
    }
}
