package com.inheritance;

public class Bottle {
    public Bottle() {
        System.out.println("Running no-arg constructor of Bottle");
    }

    public void type() {
        System.out.println("This is a general bottle");
    }

    public void material() {
        System.out.println("Bottles can be made of plastic, glass, or metal");
    }

    public void capacity() {
        System.out.println("Bottles come in various sizes like 500ml, 1L, etc.");
    }

    public void use() {
        System.out.println("Used for storing and carrying liquids");
    }

    public void color() {
        System.out.println("Bottles are available in many colors");
    }
}
