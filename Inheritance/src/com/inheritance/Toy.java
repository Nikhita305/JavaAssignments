package com.inheritance;

public class Toy {
    public Toy() {
        System.out.println("Running no-arg constructor of Toy");
    }

    public void name() {
        System.out.println("This is a toy");
    }

    public void material() {
        System.out.println("Toys are made from plastic, fabric, or rubber");
    }

    public void color() {
        System.out.println("Toys come in various bright colors");
    }

    public void purpose() {
        System.out.println("Toys are used for play and learning");
    }

    public void ageGroup() {
        System.out.println("Toys are designed for different age groups");
    }
}
