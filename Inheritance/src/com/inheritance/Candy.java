package com.inheritance;

public class Candy {
    public Candy() {
        System.out.println("Running no-arg constructor of Candy");
    }

    public void name() {
        System.out.println("Candy name");
    }

    public void flavor() {
        System.out.println("Candies come in different fruity flavors");
    }

    public void sweetness() {
        System.out.println("Candy is sweet and tasty");
    }

    public void color() {
        System.out.println("Candies are colorful and fun");
    }

    public void pack() {
        System.out.println("Candies are usually wrapped in shiny packs");
    }
}
