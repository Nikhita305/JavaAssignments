package com.inheritance;

public class Network {
    public Network() {
        System.out.println("Running no-arg constructor of Network");
    }

    public void type() {
        System.out.println("Network can be wired or wireless");
    }

    public void speed() {
        System.out.println("Network has a specific data transfer speed");
    }

    public void range() {
        System.out.println("Network has a coverage range");
    }

    public void security() {
        System.out.println("Network has encryption and security protocols");
    }

    public void connect() {
        System.out.println("Devices can connect to the network");
    }
}
