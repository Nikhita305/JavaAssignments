package com.inheritance;

public class Juice {
    public Juice() {
        System.out.println("Running no-arg constructor of Juice");
    }

    public void name() {
        System.out.println("This is a refreshing juice");
    }

    public void fruitUsed() {
        System.out.println("Juices are made using various fruits");
    }

    public void taste() {
        System.out.println("Juices are sweet, tangy, or a mix");
    }

    public void temperature() {
        System.out.println("Juices are served chilled or with ice");
    }

    public void benefit() {
        System.out.println("Juices provide hydration and nutrients");
    }
}
