package com.inheritance;

public class Shoe {
    public Shoe() {
        System.out.println("Running no-arg constructor of Shoe");
    }

    public void brand() {
        System.out.println("Generic shoe brand");
    }

    public void size() {
        System.out.println("Shoes come in different sizes");
    }

    public void type() {
        System.out.println("Shoes can be formal, casual, or sports");
    }

    public void material() {
        System.out.println("Shoes are made from leather, rubber, fabric, etc.");
    }

    public void price() {
        System.out.println("Shoe price varies based on brand and quality");
    }
}
