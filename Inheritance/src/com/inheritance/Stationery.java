package com.inheritance;

public class Stationery {
    public Stationery() {
        System.out.println("Stationery constructor called");
    }

    public void write() {
        System.out.println("Writing something...");
    }

    public void draw() {
        System.out.println("Drawing on paper...");
    }

    public void erase() {
        System.out.println("Erasing a mistake...");
    }

    public void sharpen() {
        System.out.println("Sharpening the stationery...");
    }

    public void store() {
        System.out.println("Storing stationery in the box...");
    }
}
