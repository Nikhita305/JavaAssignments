package com.inheritance;

public class Browser {
    public Browser() {
        System.out.println("Running no-arg constructor of Browser");
    }

    public void name() {
        System.out.println("This is a web browser");
    }

    public void speed() {
        System.out.println("Browser loading speed varies");
    }

    public void security() {
        System.out.println("Browsers have different security features");
    }

    public void extensions() {
        System.out.println("Browsers support different types of extensions");
    }

    public void platform() {
        System.out.println("Browsers are available on multiple platforms");
    }
}
