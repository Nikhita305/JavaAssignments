package com.inheritance;

public class Ocean {
    public Ocean() {
        System.out.println("Running no-arg constructor of Ocean");
    }

    public void waves() {
        System.out.println("Ocean creates natural waves");
    }

    public void depth() {
        System.out.println("Ocean has varying depths");
    }

    public void temperature() {
        System.out.println("Ocean temperature varies by region");
    }

    public void biodiversity() {
        System.out.println("Ocean supports marine biodiversity");
    }

    public void currents() {
        System.out.println("Ocean flows with powerful currents");
    }
}
