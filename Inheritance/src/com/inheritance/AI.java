package com.inheritance;

public class AI {
    public AI() {
        System.out.println("Running no-arg constructor of AI");
    }

    public void name() {
        System.out.println("AI name");
    }

    public void purpose() {
        System.out.println("AI is designed to mimic human intelligence");
    }

    public void learningType() {
        System.out.println("AI uses machine learning, deep learning, etc.");
    }

    public void field() {
        System.out.println("AI is used in many fields like healthcare, finance, and education");
    }

    public void interaction() {
        System.out.println("AI can interact through voice, text, or actions");
    }
}
