package com.inheritance;

public class Pet {
    public Pet() {
        System.out.println("Running no-arg constructor of Pet");
    }

    public void name() {
        System.out.println("This is a pet");
    }

    public void sound() {
        System.out.println("Pets make various sounds");
    }

    public void food() {
        System.out.println("Pets eat different types of food");
    }

    public void activity() {
        System.out.println("Pets love to play and relax");
    }

    public void sleep() {
        System.out.println("Pets need a lot of rest");
    }
}
