package com.inheritance;

public class Bird {
    public Bird() {
        System.out.println("Running no-arg constructor of Bird");
    }

    public void fly() {
        System.out.println("Bird is flying");
    }

    public void sing() {
        System.out.println("Bird is singing");
    }

    public void buildNest() {
        System.out.println("Bird is building a nest");
    }

    public void eat() {
        System.out.println("Bird is eating seeds");
    }

    public void migrate() {
        System.out.println("Bird migrates during seasons");
    }
}
