package com.inheritance;

public class Category {
    public Category() {
        System.out.println(" Category constructor called");
    }

    public void mode() {
        System.out.println("Different travel modes include flight, road, train, etc.");
    }

    public void destination() {
        System.out.println("Choosing a destination to explore.");
    }

    public void budget() {
        System.out.println("Budget depends on the travel style.");
    }

    public void duration() {
        System.out.println("Travel duration can vary from a day to months.");
    }

    public void experience() {
        System.out.println("Every travel brings a new experience.");
    }
}
