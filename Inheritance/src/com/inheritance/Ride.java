package com.inheritance;

public class Ride {
    public Ride() {
        System.out.println("Running no-arg constructor of Ride");
    }

    public void name() {
        System.out.println("This is a fun ride");
    }

    public void speed() {
        System.out.println("Rides have different speeds");
    }

    public void duration() {
        System.out.println("Each ride lasts for a certain time");
    }

    public void thrillLevel() {
        System.out.println("Thrill level varies by ride");
    }

    public void safety() {
        System.out.println("All rides follow safety protocols");
    }
}
