package com.inheritance;

public class Brand {
    public Brand() {
        System.out.println("🏷️ A brand is being initialized...");
    }

    public void name() {
        System.out.println("Generic brand name.");
    }

    public void products() {
        System.out.println("Brand has a variety of products.");
    }

    public void targetAudience() {
        System.out.println("Brand targets a wide audience.");
    }

    public void quality() {
        System.out.println("Brand offers standard quality.");
    }

    public void popularity() {
        System.out.println("Brand has a known market presence.");
    }
}
