package com.inheritance;

public class Fruit {
    public Fruit() {
        System.out.println("Running no-arg constructor of Fruit");
    }

    public void taste() {
        System.out.println("Fruit has a natural taste");
    }

    public void color() {
        System.out.println("Fruit has a variety of colors");
    }

    public void shape() {
        System.out.println("Fruits come in different shapes");
    }

    public void season() {
        System.out.println("Fruits are seasonal");
    }

    public void benefits() {
        System.out.println("Fruits are healthy and nutritious");
    }
}
