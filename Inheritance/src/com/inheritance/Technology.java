package com.inheritance;

public class Technology {
    public Technology() {
        System.out.println("Running no-arg constructor of Technology");
    }

    public void purpose() {
        System.out.println("Technology is used to solve real-world problems");
    }

    public void evolve() {
        System.out.println("Technology constantly evolves over time");
    }

    public void accessibility() {
        System.out.println("Technology is becoming more accessible");
    }

    public void innovation() {
        System.out.println("Technology is driven by innovation");
    }

    public void impact() {
        System.out.println("Technology impacts every aspect of life");
    }
}
