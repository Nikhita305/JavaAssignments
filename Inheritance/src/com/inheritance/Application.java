package com.inheritance;

public class Application {
    public Application() {
        System.out.println("Running no-arg constructor of Application");
    }

    public void name() {
        System.out.println("Generic Application");
    }

    public void platform() {
        System.out.println("Available on Android, iOS, and Web");
    }

    public void purpose() {
        System.out.println("Used for various purposes like communication, productivity, etc.");
    }

    public void userCount() {
        System.out.println("Millions of users around the world");
    }

    public void features() {
        System.out.println("Applications offer different features based on their type");
    }
}
