package com.inheritance;

public class TV {
    public TV() {
        System.out.println("Running no-arg constructor of TV");
    }

    public void brand() {
        System.out.println("Generic TV brand");
    }

    public void screenType() {
        System.out.println("TV screen types include LED, OLED, and LCD");
    }

    public void resolution() {
        System.out.println("TV resolutions include HD, Full HD, 4K, and 8K");
    }

    public void smartFeatures() {
        System.out.println("Some TVs come with smart features like apps and Wi-Fi");
    }

    public void size() {
        System.out.println("TVs come in sizes from 24 to 85 inches");
    }
}
