package com.inheritance;

public class Subject {
    public Subject() {
        System.out.println("Running no-arg constructor of Subject");
    }

    public void name() {
        System.out.println("This is a subject");
    }

    public void difficultyLevel() {
        System.out.println("Every subject has its own level of difficulty");
    }

    public void topics() {
        System.out.println("Subjects include various topics to study");
    }

    public void importance() {
        System.out.println("Subjects are important for knowledge and exams");
    }

    public void field() {
        System.out.println("Subjects belong to different fields like arts, science, etc.");
    }
}
