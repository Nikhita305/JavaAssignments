package com.inheritance;

public class Camera {
    public Camera() {
        System.out.println("Running no-arg constructor of Camera");
    }

    public void brand() {
        System.out.println("Cameras are made by different brands");
    }

    public void resolution() {
        System.out.println("Camera resolution is measured in megapixels");
    }

    public void lensType() {
        System.out.println("Different lens types for different photography needs");
    }

    public void storage() {
        System.out.println("Cameras store photos in memory cards");
    }

    public void connectivity() {
        System.out.println("Some cameras have Wi-Fi, Bluetooth, or USB connectivity");
    }
}
