package com.inheritance;

public class Mobile {
    public Mobile() {
        System.out.println("Running no-arg constructor of Mobile");
    }

    public void brand() {
        System.out.println("This is a mobile phone");
    }

    public void features() {
        System.out.println("Mobiles come with calling, internet, and apps");
    }

    public void battery() {
        System.out.println("Mobiles have rechargeable batteries");
    }

    public void camera() {
        System.out.println("Mobiles usually have front and back cameras");
    }

    public void storage() {
        System.out.println("Mobiles come with internal and external storage options");
    }
}
