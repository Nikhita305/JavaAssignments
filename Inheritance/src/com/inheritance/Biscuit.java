package com.inheritance;

public class Biscuit {
    public Biscuit() {
        System.out.println("Running no-arg constructor of Biscuit");
    }

    public void name() {
        System.out.println("Biscuit name");
    }

    public void taste() {
        System.out.println("Biscuits can be sweet or salty");
    }

    public void texture() {
        System.out.println("Biscuits are usually crunchy");
    }

    public void shape() {
        System.out.println("Biscuits come in round, square, or fun shapes");
    }

    public void serve() {
        System.out.println("Biscuits are often served with tea or milk");
    }
}
