package com.inheritance;

public class Ruler {
    public Ruler() {
        System.out.println("Ruler constructor called");
    }

    public void rule() {
        System.out.println("Ruler governs the kingdom.");
    }

    public void protect() {
        System.out.println("Ruler protects the people.");
    }

    public void strategize() {
        System.out.println("Ruler plans for battles and peace.");
    }

    public void lead() {
        System.out.println("Ruler leads with wisdom and courage.");
    }

    public void legacy() {
        System.out.println("Ruler leaves behind a lasting legacy.");
    }
}
