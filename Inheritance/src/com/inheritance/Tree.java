package com.inheritance;

public class Tree {
    public Tree() {
        System.out.println("Running no-arg constructor of Tree");
    }

    public void type() {
        System.out.println("Trees can be evergreen or deciduous");
    }

    public void height() {
        System.out.println("Trees vary in height depending on species");
    }

    public void leaves() {
        System.out.println("Trees have leaves for photosynthesis");
    }

    public void benefits() {
        System.out.println("Trees provide oxygen, shade, and prevent soil erosion");
    }

    public void lifespan() {
        System.out.println("Trees can live for many years, even centuries");
    }
}
