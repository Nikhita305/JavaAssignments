package com.inheritance;

public class Emotion {
    public Emotion() {
        System.out.println("Running no-arg constructor of Emotion");
    }

    public void type() {
        System.out.println("Emotion can be positive or negative");
    }

    public void expression() {
        System.out.println("Emotions are expressed through actions and words");
    }

    public void impact() {
        System.out.println("Emotions influence our behavior");
    }

    public void duration() {
        System.out.println("Emotions can last for a short or long time");
    }

    public void intensity() {
        System.out.println("Emotions vary in intensity");
    }
}
