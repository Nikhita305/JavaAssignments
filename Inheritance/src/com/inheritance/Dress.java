package com.inheritance;

public class Dress {
    public Dress() {
        System.out.println("Running no-arg constructor of Dress");
    }

    public void wear() {
        System.out.println("Wearing the dress");
    }

    public void wash() {
        System.out.println("Washing the dress");
    }

    public void fold() {
        System.out.println("Folding the dress");
    }

    public void iron() {
        System.out.println("Ironing the dress");
    }

    public void pack() {
        System.out.println("Packing the dress");
    }
}
