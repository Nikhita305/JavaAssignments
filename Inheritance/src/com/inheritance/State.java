package com.inheritance;

public class State {
    public State() {
        System.out.println("Running no-arg constructor of State");
    }

    public void name() {
        System.out.println("This is a state in India");
    }

    public void language() {
        System.out.println("Different states have different official languages");
    }

    public void capital() {
        System.out.println("Each state has a capital city");
    }

    public void famousFor() {
        System.out.println("States are known for their culture and landmarks");
    }

    public void population() {
        System.out.println("States have varying population sizes");
    }
}
