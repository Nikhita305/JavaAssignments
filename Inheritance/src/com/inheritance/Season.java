package com.inheritance;

public class Season {
    public Season() {
        System.out.println("Running no-arg constructor of Season");
    }

    public void name() {
        System.out.println("This is a season");
    }

    public void duration() {
        System.out.println("Seasons last for specific months");
    }

    public void temperature() {
        System.out.println("Seasons have varying temperatures");
    }

    public void activities() {
        System.out.println("Each season brings different activities");
    }

    public void clothing() {
        System.out.println("People wear season-appropriate clothes");
    }
}
