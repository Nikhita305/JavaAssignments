package com.inheritance;

public class Vegetable {
    public Vegetable() {
        System.out.println("Running no-arg constructor of Vegetable");
    }

    public void name() {
        System.out.println("This is a vegetable");
    }

    public void color() {
        System.out.println("Vegetables have different colors");
    }

    public void taste() {
        System.out.println("Vegetables have different tastes");
    }

    public void nutrients() {
        System.out.println("Vegetables are rich in nutrients");
    }

    public void season() {
        System.out.println("Some vegetables grow seasonally");
    }
}
