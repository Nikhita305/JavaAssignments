package com.inheritance;

public class Spirit {
    public Spirit() {
        System.out.println("Spirit has awakened...");
    }

    public void appear() {
        System.out.println("The spirit appears silently...");
    }

    public void vanish() {
        System.out.println("The spirit vanishes into thin air...");
    }

    public void haunt() {
        System.out.println("The spirit haunts the old mansion...");
    }

    public void whisper() {
        System.out.println("The spirit whispers softly...");
    }

    public void floatAround() {
        System.out.println("The spirit floats gracefully in the air...");
    }
}
