package com.inheritance;

public class City {
    public City() {
        System.out.println("Running no-arg constructor of City");
    }

    public void name() {
        System.out.println("This is a city");
    }

    public void population() {
        System.out.println("Cities have high population densities");
    }

    public void famousFor() {
        System.out.println("Each city is famous for something");
    }

    public void climate() {
        System.out.println("Cities have different climate conditions");
    }

    public void transportation() {
        System.out.println("Cities have various modes of transport");
    }
}
