package com.inheritance;

public class Gadget {
    public Gadget() {
        System.out.println("Running no-arg constructor of Gadget");
    }

    public void name() {
        System.out.println("This is a general gadget");
    }

    public void purpose() {
        System.out.println("Gadgets are used to make life easier and smarter");
    }

    public void batteryLife() {
        System.out.println("Battery life varies based on usage and type");
    }

    public void portability() {
        System.out.println("Most gadgets are portable and lightweight");
    }

    public void priceRange() {
        System.out.println("Price depends on features and brand");
    }
}
