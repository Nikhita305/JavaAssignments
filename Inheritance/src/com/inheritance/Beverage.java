package com.inheritance;

public class Beverage {
    public Beverage() {
        System.out.println("Running no-arg constructor of Beverage");
    }

    public void name() {
        System.out.println("This is a beverage");
    }

    public void temperature() {
        System.out.println("Beverages can be hot or cold");
    }

    public void ingredients() {
        System.out.println("Beverages are made from various ingredients");
    }

    public void taste() {
        System.out.println("Beverages have different tastes");
    }

    public void occasion() {
        System.out.println("Beverages are served at various occasions");
    }
}
