package com.inheritance;

public class Profession {
    public Profession() {
        System.out.println("Running no-arg constructor of Profession");
    }

    public void type() {
        System.out.println("This is a general profession");
    }

    public void skill() {
        System.out.println("Every profession needs a skill");
    }

    public void workHours() {
        System.out.println("Profession has specific working hours");
    }

    public void income() {
        System.out.println("Profession provides income");
    }

    public void responsibility() {
        System.out.println("Profession has responsibilities");
    }
}
