package com.inheritance;

public class Scent {
    public Scent() {
        System.out.println("Scent is being diffused...");
    }

    public void aroma() {
        System.out.println("Scent has a unique and pleasing fragrance.");
    }

    public void origin() {
        System.out.println("Derived from flowers, herbs, spices, or essential oils.");
    }

    public void strength() {
        System.out.println("Can be subtle, moderate, or strong.");
    }

    public void moodEffect() {
        System.out.println("Affects mood: calming, energizing, or romantic.");
    }

    public void use() {
        System.out.println("Used in perfumes, candles, and diffusers.");
    }
}
