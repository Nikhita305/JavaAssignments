package com.inheritance;

public class Furniture {
    public Furniture() {
        System.out.println("Running no-arg constructor of Furniture");
    }

    public void type() {
        System.out.println("This is a type of furniture");
    }

    public void material() {
        System.out.println("Furniture can be made of wood, metal, plastic, etc.");
    }

    public void usage() {
        System.out.println("Furniture is used for comfort, storage, and decor");
    }

    public void weight() {
        System.out.println("Weight depends on the type and material");
    }

    public void price() {
        System.out.println("Prices vary based on design and quality");
    }
}
