package com.inheritance;

public class Character {
    public Character() {
        System.out.println("Running no-arg constructor of Character");
    }

    public void name() {
        System.out.println("Character has a name");
    }

    public void role() {
        System.out.println("Character plays a specific role");
    }

    public void origin() {
        System.out.println("Character has an origin story");
    }

    public void strength() {
        System.out.println("Character has a unique strength");
    }

    public void costume() {
        System.out.println("Character has a costume or look");
    }
}
