package com.inheritance;

public class Creature {
    public Creature() {
        System.out.println("Running no-arg constructor of Creature");
    }

    public void habitat() {
        System.out.println("Creature lives in a specific environment");
    }

    public void diet() {
        System.out.println("Creature has a unique diet");
    }

    public void sound() {
        System.out.println("Creature makes a specific sound");
    }

    public void move() {
        System.out.println("Creature has its own way of moving");
    }

    public void defense() {
        System.out.println("Creature has some defense mechanism");
    }
}
