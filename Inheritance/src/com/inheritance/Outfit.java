package com.inheritance;

public class Outfit {
    public Outfit() {
        System.out.println("Running no-arg constructor of Outfit");
    }

    public void name() {
        System.out.println("Outfit name");
    }

    public void fabric() {
        System.out.println("Outfits come in cotton, silk, denim, etc.");
    }

    public void occasion() {
        System.out.println("Outfits can be casual, formal, or festive");
    }

    public void comfortLevel() {
        System.out.println("Comfort depends on style and material");
    }

    public void trend() {
        System.out.println("Fashion trends change every season");
    }
}
