package com.inheritance;

public class River {
    public River() {
        System.out.println("Running no-arg constructor of River");
    }

    public void name() {
        System.out.println("This is a river");
    }

    public void length() {
        System.out.println("Rivers have different lengths");
    }

    public void origin() {
        System.out.println("Rivers originate from glaciers, mountains, or springs");
    }

    public void flowDirection() {
        System.out.println("Rivers flow from higher to lower altitudes");
    }

    public void importance() {
        System.out.println("Rivers support agriculture, drinking water, and ecosystems");
    }
}
