package com.inheritance;

public class Bike {
    public Bike() {
        System.out.println("Running no-arg constructor of Bike");
    }

    public void brand() {
        System.out.println("Bike brand information");
    }

    public void speed() {
        System.out.println("Bike speed varies by model");
    }

    public void fuelType() {
        System.out.println("Most bikes use petrol");
    }

    public void gearSystem() {
        System.out.println("Bikes usually have manual gear systems");
    }

    public void mileage() {
        System.out.println("Mileage depends on engine and model");
    }
}
