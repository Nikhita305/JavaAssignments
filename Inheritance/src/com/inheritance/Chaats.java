package com.inheritance;

public class Chaats {
    public Chaats() {
        System.out.println("Running no-arg constructor of Chaats");
    }

    public void name() {
        System.out.println("This is a type of Indian chaats");
    }

    public void ingredients() {
        System.out.println("Chaats are made with spices, chutneys, and crunchy bits");
    }

    public void origin() {
        System.out.println("Chaats originated in India");
    }

    public void taste() {
        System.out.println("They taste tangy, spicy, and sweet");
    }

    public void served() {
        System.out.println("Usually served as a street food snack");
    }
}
