package com.inheritance;

public class Star {
    public Star() {
        System.out.println("Running no-arg constructor of Star");
    }

    public void name() {
        System.out.println("This is a generic star in the universe");
    }

    public void temperature() {
        System.out.println("Stars have extremely high temperatures");
    }

    public void size() {
        System.out.println("Stars vary in size from dwarf to supergiant");
    }

    public void light() {
        System.out.println("Stars emit light due to nuclear fusion");
    }

    public void position() {
        System.out.println("Stars are located throughout galaxies");
    }
}
