public class Lotion {
    public Lotion() {
        System.out.println("Running no-arg constructor of Lotion");
    }

    public void apply() {
        System.out.println("Apply lotion gently on skin");
    }

    public void fragrance() {
        System.out.println("Lotion has a soothing fragrance");
    }

    public void texture() {
        System.out.println("Lotion has a smooth and creamy texture");
    }

    public void usageTime() {
        System.out.println("Lotion can be used in the morning or before bed");
    }

    public void packaging() {
        System.out.println("Available in bottles, tubes, and pumps");
    }
}
