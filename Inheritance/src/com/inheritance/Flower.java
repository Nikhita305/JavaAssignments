package com.inheritance;

public class Flower {
    public Flower() {
        System.out.println("Running no-arg constructor of Flower");
    }

    public void bloom() {
        System.out.println("Flower is blooming");
    }

    public void smell() {
        System.out.println("Flower has a pleasant smell");
    }

    public void photosynthesize() {
        System.out.println("Flower is doing photosynthesis");
    }

    public void attractBees() {
        System.out.println("Flower attracts bees for pollination");
    }

    public void wilt() {
        System.out.println("Flower wilts over time");
    }
}
