package com.inheritance;

public class Mountain {
    public Mountain() {
        System.out.println("Running no-arg constructor of Mountain");
    }

    public void name() {
        System.out.println("This is a mountain");
    }

    public void height() {
        System.out.println("Mountains vary greatly in height");
    }

    public void location() {
        System.out.println("Mountains are found in various parts of the world");
    }

    public void climate() {
        System.out.println("Mountains have colder climates at higher altitudes");
    }

    public void importance() {
        System.out.println("Mountains affect weather, store water, and offer biodiversity");
    }
}
