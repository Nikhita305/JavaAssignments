package com.inheritance;

public class Sweet {
    public Sweet() {
        System.out.println("Running no-arg constructor of Sweet");
    }

    public void name() {
        System.out.println("This is a sweet item");
    }

    public void flavor() {
        System.out.println("Sweets have various flavors");
    }

    public void madeOf() {
        System.out.println("Sweets are made of sugar, milk, or jaggery");
    }

    public void color() {
        System.out.println("Sweets come in different colors");
    }

    public void occasion() {
        System.out.println("Sweets are enjoyed during festivals and celebrations");
    }
}
