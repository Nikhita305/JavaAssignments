package com.inheritance;

public class Color {
    public Color() {
        System.out.println("Running no-arg constructor of Color");
    }

    public void name() {
        System.out.println("This is a color");
    }

    public void wavelength() {
        System.out.println("Colors have different wavelengths");
    }

    public void emotion() {
        System.out.println("Colors can evoke different emotions");
    }

    public void usedIn() {
        System.out.println("Colors are used in art, fashion, and design");
    }

    public void combination() {
        System.out.println("Colors can be mixed to create new shades");
    }
}
