package com.inheritance;

public class Tool {
    public Tool() {
        System.out.println("Running no-arg constructor of Tool");
    }

    public void name() {
        System.out.println("Tool name");
    }

    public void material() {
        System.out.println("Tool is made of metal, plastic, or wood");
    }

    public void use() {
        System.out.println("Tools are used to perform specific tasks");
    }

    public void weight() {
        System.out.println("Tool has a certain weight depending on its type");
    }

    public void maintenance() {
        System.out.println("Tools should be cleaned and stored properly");
    }
}
