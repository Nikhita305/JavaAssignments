package com.inheritance;

public class Shampoo {
    public Shampoo() {
        System.out.println("Running no-arg constructor of Shampoo");
    }

    public void apply() {
        System.out.println("Applying the shampoo");
    }

    public void rinse() {
        System.out.println("Rinsing the shampoo");
    }

    public void foam() {
        System.out.println("Creating foam");
    }

    public void fragrance() {
        System.out.println("Mild fragrance");
    }

    public void pack() {
        System.out.println("Packing the shampoo bottle");
    }
}
