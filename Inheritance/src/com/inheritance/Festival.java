package com.inheritance;

public class Festival {
    public Festival() {
        System.out.println("Running no-arg constructor of Festival");
    }

    public void celebrate() {
        System.out.println("Celebrating the festival");
    }

    public void decorate() {
        System.out.println("Decorating homes for the festival");
    }

    public void gather() {
        System.out.println("Family and friends gather together");
    }

    public void eat() {
        System.out.println("Eating special festival food");
    }

    public void enjoy() {
        System.out.println("Enjoying the festive vibes");
    }
}
