package com.inheritance;

public class Snack {
    public Snack() {
        System.out.println("Running no-arg constructor of Snack");
    }

    public void name() {
        System.out.println("This is a tasty snack");
    }

    public void type() {
        System.out.println("Snacks can be fried, baked, or steamed");
    }

    public void ingredients() {
        System.out.println("Snacks are made using various ingredients");
    }

    public void served() {
        System.out.println("Snacks are served hot or cold");
    }

    public void popularity() {
        System.out.println("Snacks are popular all over the world");
    }
}
