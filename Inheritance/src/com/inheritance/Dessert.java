package com.inheritance;

public class Dessert {
    public Dessert() {
        System.out.println("Running no-arg constructor of Dessert");
    }

    public void name() {
        System.out.println("This is a dessert");
    }

    public void sweetnessLevel() {
        System.out.println("Desserts are usually sweet");
    }

    public void temperature() {
        System.out.println("Desserts can be hot or cold");
    }

    public void madeOf() {
        System.out.println("Desserts are made of sugar, milk, flour, etc.");
    }

    public void servedOn() {
        System.out.println("Desserts are served after meals");
    }
}
