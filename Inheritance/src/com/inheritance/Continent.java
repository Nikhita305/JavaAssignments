package com.inheritance;

public class Continent {
    public Continent() {
        System.out.println("Running no-arg constructor of Continent");
    }

    public void name() {
        System.out.println("This is a continent");
    }

    public void area() {
        System.out.println("Different continents have different land areas");
    }

    public void population() {
        System.out.println("Population varies across continents");
    }

    public void climate() {
        System.out.println("Continent has diverse climate zones");
    }

    public void countries() {
        System.out.println("A continent has multiple countries");
    }
}
