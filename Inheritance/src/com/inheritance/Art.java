package com.inheritance;

public class Art {
    public Art() {
        System.out.println("Running no-arg constructor of Art");
    }

    public void create() {
        System.out.println("Creating an artwork");
    }

    public void display() {
        System.out.println("Displaying the artwork");
    }

    public void inspire() {
        System.out.println("Art inspires people");
    }

    public void sell() {
        System.out.println("Selling the artwork");
    }

    public void preserve() {
        System.out.println("Preserving art in a gallery");
    }
}
